public class DominoStone {
    private int valueOne;
    private int valueTwo;

    public DominoStone(int valueOne, int valueTwo) {
        this.valueOne = valueOne;
        this.valueTwo = valueTwo;
    }

    public int getValueOne() {
        return valueOne;
    }

    public int getValueTwo() {
        return valueTwo;
    }


}
