public class bankAccount {
    private String name;
    private String number;
    private int money;

    public String getNumber() {
        return number;
    }
    public String getName() {
        return name;
    }
    public int getMoney() {
        return money;
    }
    public void setNumber(String n) {
        number = n;
    }
    public void setName(String n) {
        name = n;
    }
    public void setMoney(int m) {
        money = m;
    }
}
